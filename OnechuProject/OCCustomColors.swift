import UIKit

extension UIColor {
    class var OCNavy: UIColor? { return UIColor(named: "OC-Navy") }
    class var OCBlue: UIColor? { return UIColor(named: "OC-Blue") }
    class var OCDarkGrey: UIColor? { return UIColor(named: "OC-DarkGrey") }
    class var OCGreen: UIColor? { return UIColor(named: "OC-Green") }
    class var OCGrey: UIColor? { return UIColor(named: "OC-Grey") }
    class var OCLightGrey: UIColor? { return UIColor(named: "OC-LightGrey") }
    class var OCPurple: UIColor? { return UIColor(named: "OC-Purple") }
    class var OCWhiteGrey: UIColor? { return UIColor(named: "OC-WhiteGrey") }
}

